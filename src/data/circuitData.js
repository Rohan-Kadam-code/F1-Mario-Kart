/**
 * Find a circuit matching the given OpenF1 session/meeting data.
 * Tries to match on circuit name, location, country, or meeting name.
 * @param {Object} session — OpenF1 session object
 * @returns {Object|null} — Matching circuit data or null
 */
export function findCircuit(session) {
  if (!session) return null;

  // Build a single search string from all available session fields
  const searchStr = [
    session.circuit_short_name,
    session.circuit_key,
    session.country_name,
    session.location,
    session.meeting_name,
    session.session_name,
  ]
    .filter(Boolean)
    .join(' ')
    .toLowerCase();

  for (const circuit of CIRCUITS) {
    for (const key of circuit.matchKeys) {
      if (searchStr.includes(key.toLowerCase())) {
        return circuit;
      }
    }
  }

  return null;
}

/**
 * Get all available circuit IDs.
 */
export function getAvailableCircuits() {
  return CIRCUITS.map(c => ({ id: c.id, name: c.name, location: c.location }));
}

